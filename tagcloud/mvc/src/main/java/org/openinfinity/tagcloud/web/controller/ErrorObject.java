package org.openinfinity.tagcloud.web.controller;

public class ErrorObject {

	private String error_status;
	private String error_code;
	private String error_message;
	private String localized_message;
	public String getError_status() {
		return error_status;
	}
	public void setError_status(String error_status) {
		this.error_status = error_status;
	}
	public String getError_code() {
		return error_code;
	}
	public void setError_code(String error_code) {
		this.error_code = error_code;
	}
	public String getError_message() {
		return error_message;
	}
	public void setError_message(String error_message) {
		this.error_message = error_message;
	}
	public String getLocalized_message() {
		return localized_message;
	}
	public void setLocalized_message(String localized_message) {
		this.localized_message = localized_message;
	}
	

	
	
	


}
